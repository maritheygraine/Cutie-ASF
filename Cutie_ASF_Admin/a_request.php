    <script>
    function showPrompt(msg)
    {
      // CREATE A Promise TO RETURN
      var p = new Promise(function(resolve, reject) {;
      
        var dialog = $('<div/>', {class: 'popup'})
          .append(
            $('<p/>').html(msg)
          )
          .append(
            $('<div/>', {class: 'text-right'})
              .append($('<button/>', {class: 'btn btn-cancel'}).html('Decline').on('click', function() {
                $('.overlay').remove();
                // RESOLVE Promise TO false
                resolve(false);
              }))
              .append($('<button/>', {class: 'btn btn-primary'}).html('Accept').on('click', function() {
                $('.overlay').remove();
                // RESOLVE Promise TO true
                resolve(true);
              }))
          );
          
        var overlay = $('<div/>', {class: 'overlay'})
          .append(dialog);
        
        $('body').append(overlay);
        $(dialog).animate({top: '15%'}, 700);
      });
      
      // RETURN THE Promise
      return p;
    }
     
    $(function() {
      // HANDLE open-dialog CLICK
      $('.open-dialog').on('click',function(e) {
        // PREVENT DEFAULT BEHAVIOUR FOR <a/>
        e.preventDefault();
     
        // SAVE PROMISE RETURN
        var res = showPrompt('Would you like to confirm request?');
        res.then(function(ret) {
          ret ? alert('Request Accepted') : alert('Request Declined');
        })
      });
    });
